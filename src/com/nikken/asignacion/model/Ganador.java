package com.nikken.asignacion.model;

public class Ganador {

	private String codigoci;
	private String nombreci;
	private String pais;
	private String estado;
	private String rango;
	private String email;
	private String telefono;
	private String vgp;
	private String trimestrecargado;
	private String cantidadAsignado;
	
	/**
	 * @return the codigoci
	 */
	public String getCodigoci() {
		return codigoci;
	}
	/**
	 * @param codigoci the codigoci to set
	 */
	public void setCodigoci(String codigoci) {
		this.codigoci = codigoci;
	}
	/**
	 * @return the nombreci
	 */
	public String getNombreci() {
		return nombreci;
	}
	/**
	 * @param nombreci the nombreci to set
	 */
	public void setNombreci(String nombreci) {
		this.nombreci = nombreci;
	}
	/**
	 * @return the pais
	 */
	public String getPais() {
		return pais;
	}
	/**
	 * @param pais the pais to set
	 */
	public void setPais(String pais) {
		this.pais = pais;
	}
	/**
	 * @return the estado
	 */
	public String getEstado() {
		return estado;
	}
	/**
	 * @param estado the estado to set
	 */
	public void setEstado(String estado) {
		this.estado = estado;
	}
	/**
	 * @return the rango
	 */
	public String getRango() {
		return rango;
	}
	/**
	 * @param rango the rango to set
	 */
	public void setRango(String rango) {
		this.rango = rango;
	}
	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}
	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	/**
	 * @return the telefono
	 */
	public String getTelefono() {
		return telefono;
	}
	/**
	 * @param telefono the telefono to set
	 */
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	/**
	 * @return the vgp
	 */
	public String getVgp() {
		return vgp;
	}
	/**
	 * @param vgp the vgp to set
	 */
	public void setVgp(String vgp) {
		this.vgp = vgp;
	}
	/**
	 * @return the trimestrecargado
	 */
	public String getTrimestrecargado() {
		return trimestrecargado;
	}
	/**
	 * @param trimestrecargado the trimestrecargado to set
	 */
	public void setTrimestrecargado(String trimestrecargado) {
		this.trimestrecargado = trimestrecargado;
	}
	/**
	 * @return the cantidadAsignado
	 */
	public String getCantidadAsignado() {
		return cantidadAsignado;
	}
	/**
	 * @param cantidadAsignado the cantidadAsignado to set
	 */
	public void setCantidadAsignado(String cantidadAsignado) {
		this.cantidadAsignado = cantidadAsignado;
	}
	
	
}
