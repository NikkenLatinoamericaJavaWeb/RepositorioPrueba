package com.nikken.asignacion.model;

public class Report {
	 private String codigoAB;
	 private String nombreAB;
	 private String paisAB;
	 private String rangoAB;
	 private String estadoAB;
	 private String nombreProspecto;
	 private String paisProspecto;
	 private String estadoProspecto;
	 private String fechaAsignacion;
	 
	/**
	 * @return the codigoAB
	 */
	public String getCodigoAB() {
		return codigoAB;
	}
	/**
	 * @param codigoAB the codigoAB to set
	 */
	public void setCodigoAB(String codigoAB) {
		this.codigoAB = codigoAB;
	}
	/**
	 * @return the nombreAB
	 */
	public String getNombreAB() {
		return nombreAB;
	}
	/**
	 * @param nombreAB the nombreAB to set
	 */
	public void setNombreAB(String nombreAB) {
		this.nombreAB = nombreAB;
	}
	/**
	 * @return the paisAB
	 */
	public String getPaisAB() {
		return paisAB;
	}
	/**
	 * @param paisAB the paisAB to set
	 */
	public void setPaisAB(String paisAB) {
		this.paisAB = paisAB;
	}
	/**
	 * @return the rangoAB
	 */
	public String getRangoAB() {
		return rangoAB;
	}
	/**
	 * @param rangoAB the rangoAB to set
	 */
	public void setRangoAB(String rangoAB) {
		this.rangoAB = rangoAB;
	}
	/**
	 * @return the estadoAB
	 */
	public String getEstadoAB() {
		return estadoAB;
	}
	/**
	 * @param estadoAB the estadoAB to set
	 */
	public void setEstadoAB(String estadoAB) {
		this.estadoAB = estadoAB;
	}
	/**
	 * @return the nombreProspecto
	 */
	public String getNombreProspecto() {
		return nombreProspecto;
	}
	/**
	 * @param nombreProspecto the nombreProspecto to set
	 */
	public void setNombreProspecto(String nombreProspecto) {
		this.nombreProspecto = nombreProspecto;
	}
	/**
	 * @return the paisProspecto
	 */
	public String getPaisProspecto() {
		return paisProspecto;
	}
	/**
	 * @param paisProspecto the paisProspecto to set
	 */
	public void setPaisProspecto(String paisProspecto) {
		this.paisProspecto = paisProspecto;
	}
	/**
	 * @return the estadoProspecto
	 */
	public String getEstadoProspecto() {
		return estadoProspecto;
	}
	/**
	 * @param estadoProspecto the estadoProspecto to set
	 */
	public void setEstadoProspecto(String estadoProspecto) {
		this.estadoProspecto = estadoProspecto;
	}
	/**
	 * @return the fechaAsignacion
	 */
	public String getFechaAsignacion() {
		return fechaAsignacion;
	}
	/**
	 * @param fechaAsignacion the fechaAsignacion to set
	 */
	public void setFechaAsignacion(String fechaAsignacion) {
		this.fechaAsignacion = fechaAsignacion;
	}
}
